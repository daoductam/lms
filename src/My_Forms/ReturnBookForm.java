    private void jButton_Return_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Return_ActionPerformed
        // trả sách
        //cập nhật ngày trả
        //ghi chú
        // và trạng thái trả    
        // NOTE: nếu thay đổi trang thái từ LOST sang RETURN bạn cần update quantity
        //hoặc ngăn chặn user thay đổi trang thái khi LOST
        int bookId = (int) jSpinner_BookID.getValue();
        int memberId = (int) jSpinner_MemberID.getValue();
        String note = jTextArea_Note.getText();
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        
        try {
            String returnDate = dateFormat.format(jDateChooser_Return_Date.getDate()); 
            Date rtnDate = dateFormat.parse(returnDate);
            
            String issueDate = dateFormat.format(jDateChooser_IssueDate.getDate());
            Date issDate = dateFormat.parse(issueDate);
            
            
            if(rtnDate.before(issDate)) { // nếu ngày trả trước nagyf mượn
                JOptionPane.showMessageDialog(null, "Ngày trả phải sau ngày mượn","Lỗi ngày trả" ,2);
            } else {
                issueBook.updateIssue(bookId, memberId, "returned", issueDate, returnDate, note);       
                //Tải lại 
                jSpinner_BookID.setValue(0);
                jSpinner_MemberID.setValue(0);
                jLabel_BookName_.setText("Tên Sách");
                jLabel_MemberName_.setText("Tên Thành Viên");
                jDateChooser_IssueDate.setDate(new Date());
                jDateChooser_Return_Date.setDate(new Date());
                jTextArea_Note.setText("");
            }

                 
        } catch(HeadlessException | NullPointerException | ParseException ex) {
            JOptionPane.showMessageDialog(null, "Hãy chon sách từ bảng","Chọn sách" ,2);
        }

    }//GEN-LAST:event_jButton_Return_ActionPerformed

