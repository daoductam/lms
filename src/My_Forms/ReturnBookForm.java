    public void populateJTableWithIssuedBooks(String _status) {
        ArrayList<My_Classes.IssueBook> issueBooksList = issueBook.issuedBookList(_status);
        
        // Cột Jtable
        String[] colNames = {"Sách","T-Viên","T-Thái",  "N-Mượn", "N-Trả", "Ghi Chú"};
        
        // Hàng
        Object[][] rows = new  Object[issueBooksList.size()][colNames.length];
        
        for(int i =0 ; i<issueBooksList.size();i++) {
            rows[i][0] = issueBooksList.get(i).getIssueBookId();
            rows[i][1] = issueBooksList.get(i).getMemberId();
            rows[i][2] = issueBooksList.get(i).getStatus();
            rows[i][3] = issueBooksList.get(i).getIssueDate();
            rows[i][4] = issueBooksList.get(i).getReturnDate();
            rows[i][5] = issueBooksList.get(i).getNote();
            
           
        }
        
        DefaultTableModel model =  new DefaultTableModel(rows, colNames);
        jTable_IssuedBooks_.setModel(model);
        
        
    }
    
