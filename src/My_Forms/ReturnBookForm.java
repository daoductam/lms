    private void jButton_Lost_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Lost_ActionPerformed
        // TH Sách bị mất
        // chuyển trang thái về lost
        // và cập nhật số lương
        // số  lượng - 1
        // khi sách mất thì không đc thay đổi ngày trả    
        int bookId = (int) jSpinner_BookID.getValue();
        int memberId = (int) jSpinner_MemberID.getValue();
        String note = jTextArea_Note.getText();
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        
        try {
            String returnDate = dateFormat.format(jDateChooser_Return_Date.getDate()); 
           
            
            String issueDate = dateFormat.format(jDateChooser_IssueDate.getDate());
            
            
                issueBook.updateIssue(bookId, memberId, "lost", issueDate, returnDate, note);       
                
                //Cập nhật số lượng -1 
                int quantity = new My_Classes.Book().getBookById(bookId).getQuantity(); // lấy số lượng sách
                book.setQuantity_Minus_One(bookId, quantity - 1); //cập nhật só lượng sách
                //Tải lại 
                jSpinner_BookID.setValue(0);
                jSpinner_MemberID.setValue(0);
                jLabel_BookName_.setText("Tên Sách");
                jLabel_MemberName_.setText("Tên Thành Viên");
                jDateChooser_IssueDate.setDate(new Date());
                jDateChooser_Return_Date.setDate(new Date());
                jTextArea_Note.setText("");
            

                 
        } catch(HeadlessException | NullPointerException ex) {
            JOptionPane.showMessageDialog(null, "Hãy chon sách từ bảng","Chọn sách" ,2);
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }

                
    }//GEN-LAST:event_jButton_Lost_ActionPerformed

