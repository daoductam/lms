    private void jButton_search_book_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_search_book_ActionPerformed
        
        //Lấy id sách
        int bookId = (int) jSpinner_BookID.getValue();
        try {
            //Lấy sách qua id
            My_Classes.Book selectedBook = book.getBookById(bookId);
            
            if(selectedBook != null) { // nếu sách tồn tại
                // Hiển thị tên sách
                jLabel_BookName_.setText(selectedBook.getName());
                // set book exist  là true
                bookExist = true;
                
                // Hiển thị còn sản phẩm
                if(issueBook.checkBookAvailability(bookId))
                {
                    jLabel_Available.setText("CÓ");
                    jLabel_Available.setForeground(Color.GREEN);
                } else {
                    jLabel_Available.setText("KHÔNG");
                    jLabel_Available.setForeground(Color.red);
                }
            } else {
                // nếu sách này không tồn tại
                JOptionPane.showMessageDialog(null, "Sách này không tồn tại","Sách không tìm thấy",2);
                jLabel_BookName_.setText("Tên Sách");
                bookExist = false;
                jLabel_Available.setText("CÓ-hoặc-KHÔNG");
                jLabel_Available.setForeground(new Color(51,102,255));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(IssueBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }


        
          
    }//GEN-LAST:event_jButton_search_book_ActionPerformed

