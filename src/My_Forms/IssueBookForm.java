    private void jButton_Issue_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Issue_ActionPerformed
        // Mượn sách
        int bookId = (int) jSpinner_BookID.getValue();
        int memberId = (int) jSpinner_MemberID.getValue();
        String note = jTextArea_Note.getText();
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        
        try {
            String issueDate = dateFormat.format(jDateChooser_IssueDate.getDate());
            String returnDate = dateFormat.format(jDateChooser_Return_Date.getDate()); 
            // trước khi mượn ta cần phải check
            // nếu ngày trả trước ngày mượn
            // cần check nếu sách và thành viên tồn tại rồi
            Date issDate = dateFormat.parse(issueDate);
            Date rtnDate = dateFormat.parse(returnDate);
            
            
            if(!bookExist) { // nếu sách chưa tồn tại
                JOptionPane.showMessageDialog(null, "Cần kiểm tra lại nếu sách này tồn tại trước khi click search button","Kiểm tra nếu sách tồn tại" ,2);
            } else if(!memberExist){
                JOptionPane.showMessageDialog(null, "Cần kiểm tra lại nếu thành viên này tồn tại trước khi click search button","Kiểm tra nếu thành viên tồn tại" ,2);

            }
            // cần kiểm tra nếu sách này có
            else if(!issueBook.checkBookAvailability(bookId)) {
                JOptionPane.showMessageDialog(null, "Sách nàu không còn lúc này","Sách Không còn" ,2);
            }
            else if(rtnDate.before(issDate)) { // nếu ngày trả trước nagyf mượn
                JOptionPane.showMessageDialog(null, "Ngày trả phải sau ngày mượn","Lỗi ngày trả" ,2);
            } else {
                issueBook.addIssue(bookId, memberId, "issued", issueDate, returnDate, note);       
                //Tải lại 
                jSpinner_BookID.setValue(0);
                jSpinner_MemberID.setValue(0);
                jLabel_BookName_.setText("Tên Sách");
                jLabel_MemberName_.setText("Tên Thành Viên");
                jLabel_Available.setText("CÓ-hoặc-KHÔNG");
                jLabel_Available.setForeground(new Color(51,102,255));
                jDateChooser_IssueDate.setDate(new Date());
                jDateChooser_Return_Date.setDate(new Date());
                bookExist = false;
                memberExist = false;
            }

                 
        } catch(HeadlessException | NullPointerException | ParseException ex) {
            JOptionPane.showMessageDialog(null, "Hãy chon ngày mượn và ngày trả","Chọn ngày" ,2);
        }

    }//GEN-LAST:event_jButton_Issue_ActionPerformed

